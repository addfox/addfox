/** Per-target HTML defaults derived from extension manifest (Rsbuild html plugin options). */
interface ManifestHtmlDefaults {
  defaultTitle?: string;
  /**
   * Manifest `icons` entry path relative to packaged extension root (forward slashes).
   * Used to inject `<link rel="icon">` with an href relative to each HTML output path.
   */
  faviconManifestRelativePath?: string;
}

function iconSrcFromArray(icons: unknown[]): string | undefined {
  const first = icons[0];
  if (!first || typeof first !== "object") return undefined;
  const src = (first as { src?: unknown }).src;
  return typeof src === "string" ? src : undefined;
}

function iconSrcFromObjectRecord(rec: Record<string, unknown>): string | undefined {
  const entries = Object.entries(rec);
  if (entries.length === 0) return undefined;
  const numeric = entries.filter(([k]) => /^\d+$/.test(k));
  if (numeric.length > 0) {
    numeric.sort((a, b) => Number(a[0]) - Number(b[0]));
    const v = numeric[0][1];
    return typeof v === "string" ? v : undefined;
  }
  const v = entries[0][1];
  return typeof v === "string" ? v : undefined;
}

/** First manifest icon path: smallest declared size when keys are numeric, else first string value. */
function pickDefaultFaviconFromManifestIcons(icons: unknown): string | undefined {
  if (!icons || typeof icons !== "object") return undefined;
  if (Array.isArray(icons)) return iconSrcFromArray(icons);
  return iconSrcFromObjectRecord(icons as Record<string, unknown>);
}

function buildManifestHtmlDefaults(
  manifest: AddfoxResolvedConfig["manifest"],
  browser: BrowserTarget
): ManifestHtmlDefaults {
  const record = getManifestRecordForTarget(manifest, browser);
  const defaultTitle = typeof record.name === "string" ? record.name : undefined;
  const faviconManifestRelativePath = pickDefaultFaviconFromManifestIcons(record.icons);
  const out: ManifestHtmlDefaults = {};
  if (defaultTitle !== undefined) out.defaultTitle = defaultTitle;
  if (faviconManifestRelativePath !== undefined) out.faviconManifestRelativePath = faviconManifestRelativePath;
  return out;
}

/** True when manifest icon should use Rsbuild `favicon` as URL (no file emit / no relative href fix). */
function manifestIconUsesRsbuildFaviconField(icon: string): boolean {
  const t = icon.trim();
  if (!t) return false;
  return (
    t.startsWith("http://") ||
    t.startsWith("https://") ||
    t.startsWith("//") ||
    t.startsWith("data:")
  );
}

/**
 * Rsbuild / framework htmlPlugin often sets these before our pass; for auto-generated extension HTML we still
 * want manifest-driven title unless the user configured something else.
 */
const RSBUILD_DEFAULT_HTML_TITLE = "Rsbuild App";

function shouldApplyManifestTitle(htmlTitle: unknown, manifestTitle: string | undefined): manifestTitle is string {
  if (manifestTitle === undefined) return false;
  if (htmlTitle === undefined) return true;
  if (typeof htmlTitle !== "string") return false;
  return htmlTitle === RSBUILD_DEFAULT_HTML_TITLE;
}

function someEntryUsesAutoGeneratedHtml(entryByName: Map<string, EntryInfo>): boolean {
  for (const e of entryByName.values()) {
    if (entryUsesAutoGeneratedHtml(e)) return true;
  }
  return false;
}

/** Set manifest-driven `title` on an Rsbuild HTML target (root `html` or per-entry htmlPlugin options). */
function applyManifestTitleToHtmlTarget(
  target: Record<string, unknown>,
  manifestHtmlDefaults: ManifestHtmlDefaults
): void {
  if (shouldApplyManifestTitle(target.title, manifestHtmlDefaults.defaultTitle)) {
    target.title = manifestHtmlDefaults.defaultTitle;
  }
}

/** Apply manifest title/favicon defaults after `prevHtmlPlugin`. Mount id follows Rsbuild/framework defaults (typically `root`). */
function applyManifestHtmlDefaultsForAutoGeneratedEntry(
  htmlConfig: Record<string, unknown>,
  entryName: string,
  manifestHtmlDefaults: ManifestHtmlDefaults,
  entryNamesWithManifestRelativeFavicon: Set<string>
): void {
  applyManifestTitleToHtmlTarget(htmlConfig, manifestHtmlDefaults);
  const manifestIcon = manifestHtmlDefaults.faviconManifestRelativePath;
  if (manifestIcon !== undefined && htmlConfig.favicon === undefined) {
    if (manifestIconUsesRsbuildFaviconField(manifestIcon)) {
      htmlConfig.favicon = manifestIcon;
    } else {
      htmlConfig.favicon = false;
      entryNamesWithManifestRelativeFavicon.add(entryName);
    }
  }
}

/**
 * Compute `<link rel="icon" href>` for a packaged extension page.
 * Manifest paths are relative to extension root; HTML may be nested (e.g. `popup/index.html`).
 */
export function extensionIconHrefForHtmlOutput(
  htmlOutputFilename: string,
  manifestRelativeIcon: string
): string {
  const icon = manifestRelativeIcon.replace(/\\/g, "/").trim();
  if (!icon) return icon;
  if (/^https?:\/\//i.test(icon) || icon.startsWith("//") || icon.startsWith("data:")) return icon;
  if (icon.startsWith("/")) return icon;
  const htmlNorm = htmlOutputFilename.replace(/\\/g, "/");
  const htmlDir = posixDirname(htmlNorm);
  const from = htmlDir === "." ? "." : htmlDir;
  const rel = posixRelative(from, icon);
  return rel || icon;
}

function entryNameForHtmlOutputFile(outputMap: EntryOutputMap, htmlFilename: string): string | undefined {
  const norm = toWebPath(htmlFilename);
  for (const [name, out] of Object.entries(outputMap.html)) {
    if (toWebPath(out) === norm) return name;
  }
  return undefined;
}

function headTagsHaveIconLink(
  tags: readonly { tag?: string; attrs?: Record<string, string | boolean | null | undefined> }[]
): boolean {
  for (const t of tags) {
    if (t.tag !== "link" || !t.attrs) continue;
    const rel = t.attrs.rel;
    if (typeof rel !== "string") continue;
    const r = rel.toLowerCase();
    if (r === "icon" || r === "shortcut icon") return true;
  }
  return false;
}

function createManifestFaviconModifyHTMLTagsHandler(
  outputMap: EntryOutputMap,
  entryByName: Map<string, EntryInfo>,
  manifestHtmlDefaults: ManifestHtmlDefaults,
  entryNamesWithManifestRelativeFavicon: ReadonlySet<string>
): ModifyHTMLTagsFn {
  return (tags, ctx) => {
    const manifestIcon = manifestHtmlDefaults.faviconManifestRelativePath;
    if (!manifestIcon) return tags;
    if (manifestIconUsesRsbuildFaviconField(manifestIcon)) return tags;
    if (headTagsHaveIconLink(tags.headTags)) return tags;

    const htmlFilename = ctx.filename;
    if (!htmlFilename) return tags;

    const entryName = entryNameForHtmlOutputFile(outputMap, htmlFilename);
    if (!entryName) return tags;
    if (!entryNamesWithManifestRelativeFavicon.has(entryName)) return tags;

    const entry = entryByName.get(entryName);
    if (!entry || !entryUsesAutoGeneratedHtml(entry)) return tags;

    const href = extensionIconHrefForHtmlOutput(htmlFilename, manifestIcon);
    const link: HtmlBasicTag = {
      tag: "link",
      attrs: {
        rel: "icon",
        href,
        ...(href.toLowerCase().endsWith(".svg") ? { type: "image/svg+xml" } : {}),
      },
    };
    tags.headTags.unshift(link);
    return tags;
  };
}

