/** Setup rspack configuration before compiler creation. */
function setupRspackConfig(
  bundlerConfig: Record<string, unknown>,
  context: {
    root: string;
    outputRoot: string;
    outDir: string;
    htmlPaths: string[];
    outputMap: EntryOutputMap;
    entryNames: Set<string>;
    /** When true, Rspack HMR is disabled (only when addfox hotReload config is off). */
    hotReloadDisabled: boolean;
  }
): void {
  const { root, outputRoot, outDir, htmlPaths, outputMap, entryNames, hotReloadDisabled } = context;
  const distPath = resolve(root, outputRoot, outDir);

  if (hotReloadDisabled) {
    disableRspackHmrInPlace(bundlerConfig);
  } else {
    // Inject HMR noop code at the top of content/background entry chunks.
    // HMR runtime lives in the chunk bootstrap (before any module code), so
    // prepending via entry.import is too late. processAssets lets us inject
    // before the bootstrap, reliably intercepting WebSocket creation.
    const rspackConfig = bundlerConfig as { plugins?: unknown[] };
    rspackConfig.plugins = rspackConfig.plugins ?? [];
    rspackConfig.plugins.push({
      name: "rsbuild-plugin-extension-entry:hmr-noop",
      apply(compiler: any) {
        compiler.hooks.compilation.tap(
          "rsbuild-plugin-extension-entry:hmr-noop",
          (compilation: any) => {
            compilation.hooks.processAssets.tap(
              {
                name: "rsbuild-plugin-extension-entry:hmr-noop",
                stage: compilation.PROCESS_ASSETS_STAGE_ADDITIONS ?? -100,
              },
              (assets: Record<string, any>) => {
                const noopEntryNames = Array.from(entryNames).filter((n) =>
                  NO_HTML_ENTRIES.has(n)
                );
                for (const [entryName, entrypoint] of compilation.entrypoints) {
                  if (!noopEntryNames.includes(entryName)) continue;
                  for (const chunk of entrypoint.chunks) {
                    for (const file of chunk.files) {
                      if (typeof file !== "string") continue;
                      if (!file.endsWith(".js")) continue;
                      const asset = assets[file];
                      if (!asset) continue;
                      try {
                        const original = asset.source();
                        const uniqueName = compilation.outputOptions?.uniqueName || "";
                        const hotUpdateGlobal = compilation.outputOptions?.hotUpdateGlobal || `rspackHotUpdate${uniqueName}`;
                        const safeHotUpdateGlobal = hotUpdateGlobal.replace(/["\\]/g, "\\$&");
                        const hotUpdateNoop = `(function(){var g=typeof globalThis!=="undefined"?globalThis:(typeof self!=="undefined"?self:(typeof window!=="undefined"?window:this));var w=typeof window!=="undefined"?window:null;if(g&&typeof g["${safeHotUpdateGlobal}"]==="undefined"){g["${safeHotUpdateGlobal}"]=function(){};}if(w&&w!==g&&typeof w["${safeHotUpdateGlobal}"]==="undefined"){w["${safeHotUpdateGlobal}"]=function(){};}})();`;
                        const { ConcatSource, RawSource } = compiler.webpack.sources;
                        assets[file] = new ConcatSource(
                          new RawSource(HMR_NOOP_SCRIPT + "\n" + hotUpdateNoop + "\n"),
                          asset
                        );
                      } catch {
                        /* ignore asset modification errors */
                      }
                    }
                  }
                }
              }
            );
          }
        );
      },
    });
  }

  // Add template dependencies so HTML edits emit updated output files.
  if (htmlPaths.length > 0) {
    const rspackConfig = bundlerConfig as { plugins?: unknown[] };
    rspackConfig.plugins = rspackConfig.plugins ?? [];
    rspackConfig.plugins.push(createWatchTemplatesPlugin(htmlPaths));
  }

  // Ignore generated packaging output and dependencies. Large apps can emit
  // dependency file events during writes/cleanup, which otherwise retrigger dev builds.
  const watchOpts = (bundlerConfig.watchOptions as WatchOptionsLike | undefined) ?? {};
  const existingIgnored = watchOpts.ignored;
  const addfoxIgnored = buildDefaultWatchIgnores(root, outputRoot);
  const ignoredList: (string | RegExp)[] = Array.isArray(existingIgnored)
    ? [...existingIgnored, ...addfoxIgnored]
    : existingIgnored != null
      ? [existingIgnored, ...addfoxIgnored]
      : addfoxIgnored;
  bundlerConfig.watchOptions = { ...watchOpts, ignored: ignoredList };

  // Setup output configuration
  if (bundlerConfig.output) {
    const output = bundlerConfig.output as Record<string, unknown>;
    output.path = distPath;

    const jsChunkName = buildJsChunkFilenameFn(outputMap, entryNames);
    const cssChunkName = buildCssChunkFilenameFn(outputMap, entryNames);

    output.filename = jsChunkName as unknown as string;
    output.chunkFilename = output.chunkFilename ?? jsChunkName;
    output.cssFilename = cssChunkName as unknown as string;
    output.cssChunkFilename = output.cssChunkFilename ?? cssChunkName;
    output.publicPath = "/";
  }

  // Setup optimization
  if (bundlerConfig.optimization) {
    const optimization = bundlerConfig.optimization as Record<string, unknown>;
    optimization.runtimeChunk = false;
    optimization.splitChunks = optimization.splitChunks ?? {};

    const split = optimization.splitChunks as Record<string, unknown>;
    // Only HTML entries participate in splitting so background/content vendor stays in their chunks
    split.chunks = typeof split.chunks === "function"
      ? split.chunks
      : (chunk: { name?: string }) => (chunk.name ? !NO_HTML_ENTRIES.has(chunk.name) : true);
  }
}

function ensureSourceConfig(config: RsbuildConfig): SourceConfig {
  const cfg = config as RsbuildConfig & { source?: SourceConfig };
  if (!cfg.source) {
    cfg.source = {};
  }
  return cfg.source;
}

function ensureHtmlConfig(config: RsbuildConfig): HtmlConfig {
  const cfg = config as RsbuildConfig & { html?: HtmlConfig };
  if (!cfg.html) {
    cfg.html = {};
  }
  return cfg.html;
}

function ensureToolsConfig(config: RsbuildConfig): ToolsConfig {
  const cfg = config as RsbuildConfig & { tools?: ToolsConfig };
  if (!cfg.tools) {
    cfg.tools = {};
  }
  return cfg.tools;
}

/** Modify Rsbuild configuration for extension entries. */
function modifyRsbuildConfig(
  config: RsbuildConfig,
  context: {
    entry: Record<string, string | { import: string | string[]; html?: boolean }>;
    templateMap: Record<string, string>;
    scriptInjectMap: Record<string, HtmlInjectValue>;
    outputMap: EntryOutputMap;
    entryByName: Map<string, EntryInfo>;
    htmlPaths: string[];
    resolvedConfig: AddfoxResolvedConfig;
    publicDir: string;
    distPath: string;
    manifestHtmlDefaults: ManifestHtmlDefaults;
    entryNamesWithManifestRelativeFavicon: Set<string>;
    isDev: boolean;
  }
): void {
  const {
    entry,
    templateMap,
    scriptInjectMap,
    outputMap,
    entryByName,
    htmlPaths,
    resolvedConfig,
    publicDir,
    distPath,
    manifestHtmlDefaults,
    entryNamesWithManifestRelativeFavicon,
    isDev,
  } = context;

  // Setup source entry
  const source = ensureSourceConfig(config);
  source.entry = { ...(source.entry ?? {}), ...entry };

  // Setup HTML
  const html = ensureHtmlConfig(config);
  if (someEntryUsesAutoGeneratedHtml(entryByName)) {
    applyManifestTitleToHtmlTarget(html as Record<string, unknown>, manifestHtmlDefaults);
  }
  const prevTemplate = html.template;
  html.template = createTemplateHandler(templateMap, prevTemplate);
  html.inject = createInjectHandler(scriptInjectMap);
  html.outputStructure = html.outputStructure ?? "nested";

  // Setup HTML plugin
  const tools = ensureToolsConfig(config);
  const prevHtmlPlugin = tools.htmlPlugin;
  tools.htmlPlugin = createHtmlPluginHandler(
    outputMap,
    entryByName,
    prevHtmlPlugin,
    manifestHtmlDefaults,
    entryNamesWithManifestRelativeFavicon,
    isDev
  );

  // Watch HTML templates
  setupWatchFiles(config, htmlPaths);

  // Setup output (use distPath for browser-specific output)
  setupOutputConfig(config, resolvedConfig, publicDir, distPath);
}

/**
 * Main entry plugin function.
 * @param resolvedConfig - Resolved Addfox configuration
 * @param entries - Discovered entries
 * @param distPath - Output directory path (browser-specific, e.g., .addfox/extension/extension-chromium)
 */
export type EntryPluginDevOptions = {
  /** When Firefox and hot reload is on, disable Rspack HMR (use web-ext reload instead). */
  browser?: BrowserTarget;
  /** True for `addfox dev`; used to inject the Rsbuild client into HTML-only pages. */
  isDev?: boolean;
};

function shouldDisableRspackHmrForFirefox(
  _browser: BrowserTarget | undefined,
  _hotReload: AddfoxResolvedConfig["hotReload"]
): boolean {
  // Previously disabled HMR entirely for Firefox, but that prevented rsbuild
  // from re-compiling and writing files on change. Keep HMR plugin active
  // so compilation still happens; content/background chunks get noop-injected
  // to stop the HMR runtime from opening WebSocket connections.
  return false;
}

export function entryPlugin(
  resolvedConfig: AddfoxResolvedConfig,
  entries: EntryInfo[],
  distPath: string,
  devOptions?: EntryPluginDevOptions
) {
  const { outputRoot, root, appDir } = resolvedConfig;
  const publicDir = resolve(root, "public");

  // Build all lookup maps and configurations
  const entry = buildSourceEntry(entries, resolve(root, outputRoot, "html-entry-stubs"));
  const entryNames = new Set(Object.keys(entry));
  const templateMap = createTemplateMap(entries);
  const enableHtmlOnlyClient = devOptions?.isDev === true && resolvedConfig.hotReload !== false;
  const scriptInjectMap = createScriptInjectMap(entries, enableHtmlOnlyClient);
  const outputMap = buildEntryOutputMap(entries, appDir);
  const entryByName = new Map(entries.map((e) => [e.name, e]));
  const htmlPaths = entries.filter((e) => e.htmlPath).map((e) => e.htmlPath!);

  const browserTarget: BrowserTarget = devOptions?.browser ?? "chromium";
  const manifestHtmlDefaults = buildManifestHtmlDefaults(resolvedConfig.manifest, browserTarget);
  const entryNamesWithManifestRelativeFavicon = new Set<string>();

  const isDev = devOptions?.isDev === true;
  const hasScriptInjectEntry = entries.some((entry) => entry.scriptInject);

  const modifyConfigContext = {
    entry,
    templateMap,
    scriptInjectMap,
    outputMap,
    entryByName,
    htmlPaths,
    resolvedConfig,
    publicDir,
    distPath,
    manifestHtmlDefaults,
    entryNamesWithManifestRelativeFavicon,
    isDev,
  };

  return {
    name: "rsbuild-plugin-extension-entry",
    /** Run setup after default-order plugins so @rsbuild/plugin-react registers `tools.htmlPlugin` before we wrap it. */
    enforce: "post" as const,
    setup(api: RsbuildPluginAPI) {
      api.modifyRsbuildConfig((config) => {
        modifyRsbuildConfig(config, modifyConfigContext);
      });

      api.modifyHTMLTags?.({
        order: "post",
        handler: createManifestFaviconModifyHTMLTagsHandler(
          outputMap,
          entryByName,
          manifestHtmlDefaults,
          entryNamesWithManifestRelativeFavicon
        ),
      });

      if (isDev && hasScriptInjectEntry) {
        api.modifyHTML?.({
          order: "post",
          handler: createStripEntryScriptModifyHTMLHandler(outputMap, entryByName),
        });
      }

      api.onBeforeCreateCompiler(async ({ bundlerConfigs }) => {
        const bundlerConfig = bundlerConfigs[0];
        if (!bundlerConfig) return;

        setupRspackConfig(bundlerConfig, {
          root,
          outputRoot,
          outDir: distPath,
          htmlPaths,
          outputMap,
          entryNames,
          hotReloadDisabled:
            isHotReloadDisabled(resolvedConfig.hotReload) ||
            shouldDisableRspackHmrForFirefox(devOptions?.browser, resolvedConfig.hotReload),
        });
      });

      const enableHtmlPageReload =
        devOptions?.isDev === true &&
        resolvedConfig.hotReload !== false &&
        htmlPaths.length > 0;

      if (enableHtmlPageReload) {
        const templateReloadPaths = buildHtmlTemplateReloadPathMap(entries, outputMap);
        let sockWrite: HtmlFullReloadSender | null = null;
        let devCompiler: RspackCompiler | null = null;

        api.onBeforeStartDevServer?.(({ server }) => {
          sockWrite = server.sockWrite.bind(server) as HtmlFullReloadSender;
        });

        api.onAfterCreateCompiler?.(({ compiler }) => {
          devCompiler =
            typeof compiler === "object" &&
            compiler != null &&
            "compilers" in compiler &&
            Array.isArray((compiler as { compilers?: RspackCompiler[] }).compilers)
              ? ((compiler as { compilers: RspackCompiler[] }).compilers[0] ?? null)
              : (compiler as RspackCompiler);
        });

        api.onAfterDevCompile?.(async ({ stats, isFirstCompile }) => {
          if (isFirstCompile || !sockWrite || !devCompiler) return;
          if (!stats || statsHasErrors(stats)) return;

          const modifiedFiles = getModifiedFilesFromCompiler(devCompiler);
          if (modifiedFiles.size === 0) return;

          notifyHtmlTemplateFullReload(modifiedFiles, templateReloadPaths, sockWrite);
        });
      }
    },
  };
}
