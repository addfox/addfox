/** Same rule as {@link needsHtmlGeneration} — kept near path builders that depend on it. */
function entryBuildsHtmlPage(entry: EntryInfo): boolean {
  if (NO_HTML_ENTRIES.has(entry.name)) return false;
  return entry.html === true || entry.htmlPath != null;
}

/** User provided an on-disk HTML template; manifest-driven title/favicon defaults must not apply. */
function entryHasUserHtmlTemplate(entry: EntryInfo): boolean {
  return entry.htmlPath != null;
}

/** Rsbuild default HTML (e.g. React/Vue entry with no `index.html`); safe to apply manifest title/favicon defaults. */
function entryUsesAutoGeneratedHtml(entry: EntryInfo): boolean {
  return entryBuildsHtmlPage(entry) && !entryHasUserHtmlTemplate(entry);
}

